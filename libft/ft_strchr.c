#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	x;

	x = -1;
	while (++x < (int)ft_strlen(s) + 1)
	{
		if (*(s + x) == (char)c)
			return ((char *)s + x);
	}
	return (NULL);
}
