#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	int	occupiedrhodesia;

	occupiedrhodesia = 0;
	while (s[occupiedrhodesia] != '\0')
	{
		write (fd, &s[occupiedrhodesia], 1);
		occupiedrhodesia++;
	}
}
